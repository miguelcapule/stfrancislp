            </p>
            <p>
            By 1983, growing popularity led to the construction of a full elementary school building. In 1986, the high school department was established, providing continuity in education for its elementary graduates. That same year, new campuses were founded in Taguig City and Los Baños, Laguna.
            </p>
            <p>
            In 1989, SFAC expanded into tertiary education, offering both degree and non-degree programs. More campuses followed: Pamplona (1990), Bacoor (1994), and Alabang (1996). In the same year, the Saint Anthony School of Las Piñas also came under SFAC’s management.
            </p>
            <p>
            The Dasmariñas campus opened in 1999, followed by Biñan in 2006. In 2010, the Los Baños campus was renamed Saint Anthony School of Los Baños, honoring the local patron saint. The Sta. Rosa campus began operations in S.Y. 2012–2013. In 2014, the Pamplona campus merged with the main campus as part of a corporate branding strategy.
